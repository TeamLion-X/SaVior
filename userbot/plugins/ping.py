menu_category = "tools"


@savior.savior_cmd(
    pattern="ping( -a|$)",
    command=("ping", menu_category),
    info={
        "header": "check how long it takes to ping your userbot",
        "flags": {"-a": "average ping"},
        "usage": ["{tr}ping", "{tr}ping -a"],
    },
)
async def _(event):
    "To check ping"
    type = event.pattern_match.group(1)
    start = datetime.now()
    if type == " -a":
        saviorevent = await eor(event, "`!....`")
        await asyncio.sleep(0.3)
        await saviorevent.edit("`..!..`")
        await asyncio.sleep(0.3)
        await saviorevent.edit("`....!`")
        end = datetime.now()
        tms = (end - start).microseconds / 1000
        ms = round((tms - 0.6) / 3, 3)
        await saviorevent.edit(f"**✨Average Pong!**\n➥ {ms} ms")
    else:
        redeye = (
            gvarstatus("PING_IMG")
            or "https://telegra.ph/file/e15a2fe1430358e26713c.jpg"
        )
        llol = [x for x in redeye.split()]
        IPIC = random.choice(llol)
        if redeye == "OFF":
            saviorevent = await eor(event, "<b><i>⚡ **Pong!** ⚡</b></i>", "html")
            end = datetime.now()
            ms = (end - start).microseconds / 1000
            await saviorevent.edit(
                f"<b><i>✨Pong </b></i>\n\n   🕯️ {ms} <b><i>ms\n   Bot : {hmention}</b></i>",
                parse_mode="html",
            )
        else:
            saviorevent = await eor(event, "<b><i>⚡ **Pong!** ⚡</b></i>", "html")
            end = datetime.now()
            ms = (end - start).microseconds / 1000
            await saviorevent.delete()
            await event.client.send_file(
                event.chat_id,
                IPIC,
                caption=f"<b><i>✨Pong </b></i>\n\n   🕯️ {ms} <b><i>ms\n   Bot : {hmention}</b></i>",
                parse_mode="html",
            )


